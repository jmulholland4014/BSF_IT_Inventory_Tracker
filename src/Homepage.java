    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String dataRequested = "";
        if(SuppliesPnl.isVisible()){
            dataRequested = "supplies";
        }
        else if (UsersPnl.isVisible()){
            dataRequested = "users";
        }
        else if (SuppliersPnl.isVisible()){
            dataRequested = "suppliers";
        }
        else{
            System.out.println("Error: No panel is visible");
        }
        HashMap<String,String> result = backend.fetchPnlObjectItems(itemText.getText(), dataRequested);
        if(result != null){
            if(dataRequested.equals("supplies")){
                itemNameLbl.setText(result.get("Name")); 
                itemModelLbl.setText(result.get("Model"));
                deviceLbl.setText(result.get("Device"));
                currOwnerLbl.setText(result.get("Owner"));
                passwordLbl.setText(result.get("Password"));
            }
            if(dataRequested.equals("suppliers")){
                supplierNameLbl.setText(result.get("Name"));
                supplierAddressLbl.setText(result.get("Address"));
                supplierPhoneLbl.setText(result.get("Phone"));
                supplierEmailLbl.setText(result.get("Email"));
                suppliesLbl.setText(result.get("itemsSupplied"));
            }
            if(dataRequested.equals("users")){
                empNameLbl.setText(result.get("Name"));
                empDevicesLbl.setText(result.get("Devices"));
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

