    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        System.out.println("Searched");
        String dataRequested = "";
        if(SuppliesPnl.isVisible()){
            dataRequested = "supplies";
        }
        else if (UsersPnl.isVisible()){
            dataRequested = "users";
        }
        else if (SuppliersPnl.isVisible()){
            dataRequested = "suppliers";
        }
        else if (MaintenancePnl.isVisible()){
            dataRequested = "maintenance";
        }
        else{
            System.out.println("Error: No panel is visible");
        }
        
        String ID;
        switch(dataRequested) {
            case "supplies":
                ID = supplyTxt.getText();
                break;
            case "suppliers":
                ID = supplierTxt.getText();
                break;
            case "users":
                ID = empIDText.getText();
                break;
            default:
                ID = mtncIDTxt.getText();
                break;
        }
        
        HashMap<String,String> result = backend.fetchPnlObjectItems(ID, dataRequested);
        if(result != null){
            if(dataRequested.equals("supplies")){
                itemNameLbl.setText(result.get("Condition")); 
                itemModelLbl.setText(result.get("Model"));
                deviceLbl.setText(result.get("Type"));
                currOwnerLbl.setText(result.get("Status"));
                passwordLbl.setText(result.get("Access"));
                
                if (result.get("Status").equals("UNASSIGNED")) {
                    checkOutBtn.setText("Check Out");
                } else {
                    checkOutBtn.setText("Check In");
                }
                
                // Initialize checkOutPnl
                if (checkOutPnl == null) {
                    checkOutPnl = new CheckInOutSupply();
                }
                
                checkOutPnl.initialize(this.userID, result.get("Type"), ID, result.get("Status").equals("UNASSIGNED"));
            }
            if(dataRequested.equals("suppliers")){
                supplierNameLbl.setText(result.get("Name"));
                supplierAddressLbl.setText(result.get("Address"));
                supplierPhoneLbl.setText(result.get("Phone"));
                supplierEmailLbl.setText(result.get("Email"));
                suppliesLbl.setText(result.get("itemsSupplied"));
            }
            if(dataRequested.equals("users")){
                empNameLbl.setText(result.get("Name"));
                empDevicesLbl.setText(result.get("Devices"));
            }
            if(dataRequested.equals("maintenance")){
                issueLbl.setText(result.get("issue"));
                costLbl.setText(result.get("cost"));
                fixedByLbl.setText(result.get("fixedBy"));
                deviceSNLbl.setText(result.get("deviceSN"));
                fixedDateLbl.setText(result.get("fixedDate"));
                locationLbl.setText(result.get("location"));
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

